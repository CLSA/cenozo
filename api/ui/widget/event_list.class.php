namespace cenozo\ui\widget;
use cenozo\lib, cenozo\log;

/**
 * widget event list
 */
class event_list extends base_list
{
  /**
   * Constructor
   * 
   * Defines all variables required by the event list.
   * @author Patrick Emond <emondpd@mcmaster.ca>
   * @param array $args An associative array of arguments to be processed by the widget
   * @access public
   */
  public function __construct( $args )
  {
    parent::__construct( 'event', $args );
  }

  /**
   * Processes arguments, preparing them for the operation.
   * 
   * @author Patrick Emond <emondpd@mcmaster.ca>
   * @throws exception\notice
   * @access protected
   */
  protected function prepare()
  {
    parent::prepare();
    
    $this->add_column( 'event_type.name', 'string', 'Event', true );
    $this->add_column( 'datetime', 'datetime', 'Date', true );
  }
  
  /**
   * Finish setting the variables in a widget.
   * 
   * @author Patrick Emond <emondpd@mcmaster.ca>
   * @access protected
   */
  protected function setup()
  {
    parent::setup();

    foreach( $this->get_record_list() as $record )
    {
      $this->add_row( $record->id,
        array( 'event_type.name' => $record->get_event_type()->name,
               'datetime' => $record->datetime ) );
    }
  }
}
